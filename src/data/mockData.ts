function makeChapters(prefix: string, count: number): Chapter[] {
  const chapters: Chapter[] = [];
  for (let i = 1; i <= count; i++) {
    const lessons = [];
    const lessonCount = 4 + Math.floor(Math.random() * 4);
    for (let j = 1; j <= lessonCount; j++) {
      lessons.push({
        id: `${prefix}-c${i}-l${j}`,
        title: `第 ${j} 节：${['基础概念', '核心原理', '实战案例', '进阶技巧', '最佳实践', '常见问题', '项目演练', '总结回顾'][j - 1] || '课程内容'}`,
        duration: `${Math.floor(Math.random() * 20) + 10}:${String(Math.floor(Math.random() * 60)).padStart(2, '0')}`,
        isPreview: i === 1 && j <= 2,
      });
    }
    chapters.push({
      id: `${prefix}-c${i}`,
      title: `第 ${i} 章 ${['课程导论与环境搭建', '核心知识体系精讲', '项目实战：从零到一', '性能优化与工程化', '高级特性与扩展', '综合项目答辩'][i - 1] || '章节标题'}`,
      lessons,
    });
  }
  return chapters;
}

